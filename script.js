function iniciarSite() {
    const audio = document.getElementById("musicaFundo");
    audio.play().catch(() => console.log("Aguardando interação"));
    
    const overlay = document.getElementById("overlay");
    overlay.style.opacity = "0";
    overlay.style.transform = "scale(2)";
    setTimeout(() => {
        overlay.style.display = "none";
        document.getElementById("site").classList.remove("escondido");
    }, 1500);
}

function atualizarContador() {
    const agora = new Date();
    const diff = agora - dataInicio;
    const d = Math.floor(diff / (1000 * 60 * 60 * 24));
    const h = Math.floor((diff / (1000 * 60 * 60)) % 24);
    const m = Math.floor((diff / (1000 * 60)) % 60);
    const s = Math.floor((diff / 1000) % 60);
    
    // Frase personalizada
    document.getElementById("contador").innerText = `Junto ao amor da minha vida a ${d}D ${h}H ${m}M ${s}S`;
}
setInterval(atualizarContador, 1000);

